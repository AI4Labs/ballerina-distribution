# Start Ballerina message broker using `broker.sh` or `broker.bat` command
$ broker.sh

# At the command line, navigate to the directory that contains the
# `jms-queue-message-receiver-with-client-acknowledgment.bal` file and run the `ballerina run` command.
$ ballerina run jms-queue-message-receiver-with-client-acknowledgment.bal

#JMS queue receiver run as a Ballerina service listening on the subscribed queue.
